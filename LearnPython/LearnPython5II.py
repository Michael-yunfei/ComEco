# Seaborn is a Python data visualization library based on matplotlib.
# It provides a high-level interface for drawing attractive and
# informative statistical graphics.
# plotnine is an implementation of a grammar of graphics in Python,
# it is based on ggplot2. The grammar allows users to compose plots
# by explicitly mapping data to the visual objects that make up the plot.
